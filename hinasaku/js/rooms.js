window.addEventListener('load', function(){
// Gallery Crossfade
document.querySelectorAll('.hn-gallery').forEach(function(gallery){
  var items=gallery.querySelectorAll('.hn-gallery-item');
  var thumbsC=gallery.parentElement.querySelector('.hn-gallery-thumbs');
  var vis=[];
  items.forEach(function(it){
    var img=it.querySelector('img');
    if(img&&img.src){it.style.display='';vis.push(it);}
    else{it.style.display='none';}
  });
  if(vis.length>0&&!vis.some(function(v){return v.classList.contains('active');})){vis[0].classList.add('active');}
  if(thumbsC){
    thumbsC.innerHTML='';
    vis.forEach(function(item,i){
      var img=item.querySelector('img');
      var t=document.createElement('div');
      t.className='hn-gallery-thumb'+(item.classList.contains('active')?' active':'');
      t.innerHTML='<img src="'+img.src+'" alt="">';
      t.addEventListener('click',function(){goTo(i);});
      thumbsC.appendChild(t);
    });
  }
  var idx=0;
  function goTo(i){
    idx=((i%vis.length)+vis.length)%vis.length;
    vis.forEach(function(v,j){v.classList.toggle('active',j===idx);});
    if(thumbsC){thumbsC.querySelectorAll('.hn-gallery-thumb').forEach(function(t,j){t.classList.toggle('active',j===idx);});}
  }
  if(vis.length>1){
    var auto=setInterval(function(){goTo(idx+1);},4000);
    gallery.addEventListener('mouseenter',function(){clearInterval(auto);});
    gallery.addEventListener('mouseleave',function(){auto=setInterval(function(){goTo(idx+1);},4000);});
  }
});
// Scroll Reveal
var srObs=new IntersectionObserver(function(entries){
  entries.forEach(function(e){if(e.isIntersecting){e.target.classList.add('hn-vis');srObs.unobserve(e.target);}});
},{threshold:0.15});
document.querySelectorAll('.hn-sr').forEach(function(el){srObs.observe(el);});
});
