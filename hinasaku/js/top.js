// Slider
document.querySelectorAll('.hn-slider').forEach(function(slider){
  var track=slider.querySelector('.hn-slider-track');
  if(!track)return;
  var items=track.querySelectorAll('.hn-slider-item');
  var idx=0;
  function getCount(){var c=0;items.forEach(function(it){if(it.style.display!=='none')c++;});return c;}
  function go(i){var t=getCount();if(t<2)return;idx=((i%t)+t)%t;track.style.transform='translateX(-'+idx*100+'%)';}
  var prev=slider.querySelector('.hn-slider-arrow.prev');
  var next=slider.querySelector('.hn-slider-arrow.next');
  if(prev)prev.addEventListener('click',function(){go(idx-1);});
  if(next)next.addEventListener('click',function(){go(idx+1);});
  var auto=setInterval(function(){go(idx+1);},5000);
  slider.addEventListener('mouseenter',function(){clearInterval(auto);});
  slider.addEventListener('mouseleave',function(){auto=setInterval(function(){go(idx+1);},5000);});
});
// Scroll Reveal
